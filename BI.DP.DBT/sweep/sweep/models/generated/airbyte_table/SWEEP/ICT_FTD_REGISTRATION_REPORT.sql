{{ config(
    materialized = "table",
    cluster_by = ["_AIRBYTE_UNIQUE_KEY", "_AIRBYTE_NORMALIZED_AT"],
    unique_key = "_AIRBYTE_UNIQUE_KEY",
    database = env_var('RAW_DATABASE'),
    schema = "SWEEP",
    tags = [ "top-level" ],
) }}
SELECT
    REG._AIRBYTE_UNIQUE_KEY,
    REG.DATE,
    REG.AFP,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.COMMISSION ELSE REG.COMMISSION END AS COMMISSION,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN  FTD.COMMISSIONS ELSE REG.COMMISSIONS END AS COMMISSIONS,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.DEPOSITS ELSE REG.DEPOSITS END DEPOSITS,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.DEPOSIT_COUNT ELSE REG.DEPOSIT_COUNT END AS DEPOSIT_COUNT,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.EXTERNAL_DATE ELSE REG.EXTERNAL_DATE END AS EXTERNAL_DATE,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.FIRST_DEPOSIT ELSE REG.FIRST_DEPOSIT END AS FIRST_DEPOSIT,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.FIRST_DEPOSIT_DATE ELSE REG.FIRST_DEPOSIT_DATE END AS FIRST_DEPOSIT_DATE,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.NET_DEPOSITS ELSE REG.NET_DEPOSITS END AS NET_DEPOSITS,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.QUALIFICATION_DATE ELSE REG.QUALIFICATION_DATE END AS QUALIFICATION_DATE ,
    CASE WHEN FTD._AIRBYTE_UNIQUE_KEY IS NOT NULL THEN FTD.WITHDRAWALS ELSE REG.WITHDRAWALS END AS WITHDRAWALS,
    0 as PL,
    REG.GENERIC_1,
    REG.GENERIC_2,
    REG.COUNTRY,
    REG.REGISTRATION_DATE,
    REG.STATUS,
    REG.TRACKING_CODE,
    REG.USERID,
    REG.TRACKER_LOGIN_ID,
    REG._AIRBYTE_AB_ID AS _REG_AIRBYTE_AB_ID,
    REG._AIRBYTE_EMITTED_AT AS _REG_AIRBYTE_EMITTED_AT,
    _FTD_REGISTRATION_REPORT__AIRBYTE_AB_ID,
    _FTD_AIRBYTE_EMITTED_AT,
    GREATEST_IGNORE_NULLS(FTD._AIRBYTE_NORMALIZED_AT, REG._AIRBYTE_NORMALIZED_AT) AS _AIRBYTE_NORMALIZED_AT,
    CASE WHEN  _FTD_REGISTRATION_REPORT__AIRBYTE_AB_ID IS NOT NULL THEN  _FTD_REGISTRATION_REPORT__AIRBYTE_AB_ID ELSE REG._AIRBYTE_AB_ID END AS  _AIRBYTE_AB_ID,
    GREATEST_IGNORE_NULLS(_FTD_AIRBYTE_EMITTED_AT, REG._AIRBYTE_EMITTED_AT) AS  _AIRBYTE_EMITTED_AT,
    _AIRBYTE_REGISTRATION_REPORT_HASHID,
    _AIRBYTE_FTD_REGISTRATION_REPORT_HASHID
FROM {{ ref('REGISTRATION_REPORT') }} AS REG
LEFT JOIN {{ ref('FTD_REGISTRATION_REPORT') }} AS FTD 
ON FTD._AIRBYTE_UNIQUE_KEY = REG._AIRBYTE_UNIQUE_KEY