{{ config(
    materialized = "table",
    cluster_by = ["_AIRBYTE_UNIQUE_KEY", "_AIRBYTE_EMITTED_AT"],
    database = env_var('RAW_DATABASE'),
    schema = "CELLXPERT",
    tags = [ "top-level" ],
) }}
SELECT
    REG._AIRBYTE_UNIQUE_KEY,
    REG.DATE,
    REG.AFP,
    COALESCE(FTD.COMMISSION, REG.COMMISSION) AS COMMISSION,
    COALESCE(FTD.COMMISSIONS, REG.COMMISSIONS) AS COMMISSIONS,
    COALESCE(FTD.DEPOSITS, REG.DEPOSITS) AS DEPOSITS,
    COALESCE(FTD.DEPOSIT_COUNT, REG.DEPOSIT_COUNT) AS DEPOSIT_COUNT,
    COALESCE(FTD.EXTERNAL_DATE, REG.EXTERNAL_DATE) AS EXTERNAL_DATE,
    COALESCE(FTD.FIRST_DEPOSIT, REG.FIRST_DEPOSIT) AS FIRST_DEPOSIT,
    COALESCE(FTD.FIRST_DEPOSIT_DATE, REG.FIRST_DEPOSIT_DATE) AS FIRST_DEPOSIT_DATE,
    COALESCE(FTD.NET_DEPOSITS, REG.NET_DEPOSITS) AS NET_DEPOSITS,
    COALESCE(FTD.QUALIFICATION_DATE, REG.QUALIFICATION_DATE) AS QUALIFICATION_DATE,
    COALESCE(FTD.WITHDRAWALS, REG.WITHDRAWALS) AS WITHDRAWALS,
    0 as PL,
    REG.GENERIC_1,
    REG.GENERIC_2,
    REG.COUNTRY,
    REG.REGISTRATION_DATE,
    REG.STATUS,
    REG.TRACKING_CODE,
    REG.USERID,
    REG.TRACKER_LOGIN_ID,
    REG._AIRBYTE_AB_ID AS _REG_AIRBYTE_AB_ID,
    REG._AIRBYTE_EMITTED_AT _REG_AIRBYTE_EMITTED_AT,
    FTD._AIRBYTE_AB_ID AS _FTD_AIRBYTE_AB_ID,
    FTD._AIRBYTE_EMITTED_AT AS _FTD_AIRBYTE_EMITTED_AT,
    {{ current_timestamp() }} as _AIRBYTE_NORMALIZED_AT,
    COALESCE(FTD._AIRBYTE_AB_ID, REG._AIRBYTE_AB_ID) AS _AIRBYTE_AB_ID,
    COALESCE(FTD._AIRBYTE_EMITTED_AT, REG._AIRBYTE_EMITTED_AT) AS _AIRBYTE_EMITTED_AT,
    _AIRBYTE_REGISTRATION_REPORT_HASHID,
    _AIRBYTE_FTD_REGISTRATION_REPORT_HASHID
FROM {{ ref('REGISTRATION_REPORT') }} AS REG
LEFT JOIN  {{ ref('FTD_REGISTRATION_REPORT') }} AS FTD 
    ON DATE(FTD.DATE) = DATE(REG.DATE)
    AND FTD.USERID = REG.USERID
    AND FTD.TRACKER_LOGIN_ID = REG.TRACKER_LOGIN_ID

